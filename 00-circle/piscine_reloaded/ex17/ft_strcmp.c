int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	diff;

	i = 0;
	while (1)
	{
		diff = s1[i] - s2[i];
		if (diff || s1[i] == 0 || s2[i] == 0)
			return (diff);
		i++;
	}
	return (0);
}
